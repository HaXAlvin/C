#include<stdio.h>
#include<stdlib.h>
#include<string.h>
typedef struct node{
    int type;
    struct node *nextptr;
    union{
        int a;
        float b;
        char c[16];

    }center;

}arr;
void change(arr *temp,char temp1){//call by reference
    char inputchar[16];//暫存字串空間的變數
    if (temp1=='i'){//型態為整數
        temp->type=1;//紀錄型態為整數
        scanf("%d",&(temp->center).a);//將輸入資料存進結構的a
        temp->nextptr=NULL;
        return;
    }
    else if(temp1=='f'){//型態為小數
        temp->type=2;//型態為小數
        scanf("%f",&(temp->center).b);//將輸入資料存進結構的b
        temp->nextptr=NULL;
        return;
    }
    else {
        temp->type=3;//型態為字串
        scanf("%s",&inputchar);//輸入
        strcpy((temp->center).c,inputchar);//copy進結構的c
        temp->nextptr=NULL;
        return;
    }

}

int main(){
    arr *data,*crtdata;//指標結構
    data=NULL;
    crtdata=NULL;
    int i;
    int input;//選擇功能，輸入的資料
    char inputtype;//資料型態

    while(1){
        printf("選擇:(1.新增/2.印出/0.結束):");
        scanf("%d",&input);
        if(input>2){//防呆
            printf("輸入錯誤!");
            continue;
        }
        if (input==0){//為0結束程式

            free(data);
            break;
        }
        switch(input){
            case 1:
                if(data==NULL){//新增記憶體空間
                    data=malloc(sizeof(arr));
                    crtdata=data;
                }

                else{
                    crtdata->nextptr=malloc(sizeof(arr));
                    crtdata=crtdata->nextptr;
                }


                printf("選擇類型:(i = int/f = float/s = char[16]):");
                getchar();
                scanf("%c",&inputtype);//輸入類型
                if ((inputtype!='i')&&(inputtype!='f')&&(inputtype!='s')){//防呆
                    printf("輸入錯誤!\n");
                    continue;
                }
                printf("輸入資料:");
                if (inputtype=='i'){//整數
                    change(crtdata,inputtype);
                }
                else if (inputtype=='f'){//小數
                    change(crtdata,inputtype);
                }
                else{//字串
                   change(crtdata,inputtype);
                }
                break;
            case 2:
                crtdata=data;
                while(crtdata!=NULL){//印出所存的資料
                    if (crtdata->type==1)
                        printf("%d\n",crtdata->center.a);

                    else if (crtdata->type==2)
                        printf("%f\n",crtdata->center.b);
                    else
                        printf("%s\n",crtdata->center.c);

                    crtdata=crtdata->nextptr;


                }
                break;
        }

    }

}


